include Stdlib.Option

module Minimal = struct
  type 'a t = 'a option
  let return v = Some v
  let bind f m = bind m f
  let product l r = match l, r with Some l, Some r -> Some (l, r) | _ -> None
end

include Monad.Make_based_on_bind_with_product (Minimal)
let bind m f = bind f m
